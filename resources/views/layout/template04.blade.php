                <h2 class="deskripsi">Definisi Sensor and Transduser</h2>
                <h4>Pengertian Sensor</h4>
                <p>Secara umum sensor didefenisikan sebagai alat yang mampu menangkap fenomena
                    fisika atau kimia kemudian mengubahnya menjadi sinyal electrik baik arus listrik ataupun
                    tegangan. Fenomena fisik yang mampu menstimulus sensor untuk menghasilkan sinyal
                    electrik meliputi temperatur, tekanan, gaya, medan magnet cahaya, pergerakan dan
                    sebagainya. Sensor adalah komponen elektronika yang berfungsi untuk mengubah
                    besaran mekanis, magnetis, panas, sinar, dan kimia menjadi besarn listrik berupa
                    tegangan, resistansi dan arus listrik. Sensor sering digunakan untuk pendeteksian pada
                    saat melakukan pengukuran atau pengendalian.</p>
                    <p>D Sharon, dkk (1982), mengatakan sensor adalah suatu peralatan yang berfungsi
                        untuk mendeteksi gejala-gejala atau sinyal-sinyal yang berasal dari perubahan suatu
                        energi seperti energi listrik, energi fisika, energi kimia, energi biologi, energi mekanik dan
                        sebagainya</p>
                <h4>Pengertian Transduser</h4>
                        <p>Transduser berasal dari kata “traducere” dalam bahasa Latin yang berarti mengubah. 
                            Sehingga transduser dapat didefinisikan sebagai suatu peranti yang dapat mengubah 
                            suatu energi ke bentuk energi yang lain. Suatu definisi mengatakan “transducer adalah
                            alat yang bila digerakkan oleh energi di dalam sebuah sitem transmisi, 
                            menyalurkan energi dalam bentuk yang sama atau dalam bentuk yang berlainan ke
                            sistem transmisi kedua”.Transmisi kedua ini bisa listrik, mekanik, kimia, optik (radiasi)
                            atau termal (panas).</p>
